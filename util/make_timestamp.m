function timestamp = make_timestamp()
% *************************************************************************
% *************************************************************************
% *************************************************************************
%
% MAKE TIME STAMP FOR RELATIVE FILE PATH
%
% Brent Wallace  
%
% 2021-12-13
%
% *************************************************************************
% *************************************************************************
% *************************************************************************

time = fix(clock);
timestamp = '';
for i = 1:length(time)
   timestamp = [timestamp , num2str(time(i))];
   if i < length(time)
       timestamp = [timestamp , '_'];
   end
end
timestamp = [timestamp, '/'];
